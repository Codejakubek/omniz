#ifndef SD_MISC_COLORS_H
#define SD_MISC_COLORS_H

/**
 * It contains the linux terminal color thingies.
 */

#define SD_LINUX_TERMINAL_COLOR_RESET           "\033[0m"
#define SD_LINUX_TERMINAL_COLOR_BLACK           "\033[30m"      // Black 
#define SD_LINUX_TERMINAL_COLOR_RED             "\033[31m"      // Red
#define SD_LINUX_TERMINAL_COLOR_GREEN           "\033[32m"      // Green
#define SD_LINUX_TERMINAL_COLOR_YELLOW          "\033[33m"      // Yellow
#define SD_LINUX_TERMINAL_COLOR_BLUE            "\033[34m"      // Blue
#define SD_LINUX_TERMINAL_COLOR_MAGENTA         "\033[35m"      // Magenta
#define SD_LINUX_TERMINAL_COLOR_CYAN            "\033[36m"      // Cyan
#define SD_LINUX_TERMINAL_COLOR_WHITE           "\033[37m"      // White
#define SD_LINUX_TERMINAL_COLOR_BOLDBLACK       "\033[1m\033[30m"       // Bold Black
#define SD_LINUX_TERMINAL_COLOR_BOLDRED         "\033[1m\033[31m"       // Bold Red
#define SD_LINUX_TERMINAL_COLOR_BOLDGREEN       "\033[1m\033[32m"       // Bold Green
#define SD_LINUX_TERMINAL_COLOR_BOLDYELLOW      "\033[1m\033[33m"       // Bold Yellow
#define SD_LINUX_TERMINAL_COLOR_BOLDBLUE        "\033[1m\033[34m"       // Bold Blue
#define SD_LINUX_TERMINAL_COLOR_BOLDMAGENTA     "\033[1m\033[35m"       // Bold Magenta
#define SD_LINUX_TERMINAL_COLOR_BOLDCYAN        "\033[1m\033[36m"       // Bold Cyan
#define SD_LINUX_TERMINAL_COLOR_BOLDWHITE       "\033[1m\033[37m"       // Bold White

#endif